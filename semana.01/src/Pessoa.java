/**
 * Pessoa
 */
public class Pessoa {

    private int id;
    private char nome;
    private char endereco;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public char getNome() {
        return nome;
    }

    public void setNome(char nome) {
        this.nome = nome;
    }

    public char getEndereco() {
        return endereco;
    }

    public void setEndereco(char endereco) {
        this.endereco = endereco;
    }
}